def add_numbers(num1, num2):
    """
    Calculate the sum of two numbers and print the result.
    
    Parameters:
        num1 (int): The first number
        num2 (int): The second number
    """
    sum_value = num1 + num2

    # Print the result
    print(f'The sum of {num1} and {num2} is {sum_value}.')


if __name__ == '__main__':
    add_numbers(10, 20)